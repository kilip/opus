// Package gofiber implements the HTTP delivery layer for the Opus server using GoFiber v3.
package gofiber

import (
	"fmt"

	"github.com/gofiber/fiber/v3"
)

// Envelope represents the standard API response structure as per ADR-004.
type Envelope[T any] struct {
	// Data holds the primary payload of the response.
	Data T `json:"data"`

	// Error holds the RFC 7807 problem details if an error occurred.
	Error *Problem `json:"error"`

	// Meta holds additional response metadata such as tracing and pagination information.
	Meta *Meta `json:"meta"`
}

// Problem represents an RFC 7807 Problem Details object.
type Problem struct {
	// Type is a URI reference that identifies the problem type.
	Type string `json:"type"`

	// Title is a short, human-readable summary of the problem type.
	Title string `json:"title"`

	// Status is the HTTP status code generated by the origin server for this occurrence.
	Status int `json:"status"`

	// Detail is a human-readable explanation specific to this occurrence of the problem.
	Detail string `json:"detail"`

	// Instance is a URI reference that identifies the specific occurrence of the problem.
	Instance string `json:"instance"`
}

// Meta contains metadata for the response, such as pagination and tracing information.
type Meta struct {
	// Cursor contains cursor-based pagination details.
	Cursor *CursorMeta `json:"cursor,omitempty"`

	// Total is the total number of records available (optional).
	Total *int64 `json:"total,omitempty"`

	// RequestID is the unique identifier for the request, used for tracing.
	RequestID string `json:"request_id"`
}

// CursorMeta contains cursor-based pagination details.
type CursorMeta struct {
	// Next is the cursor string pointing to the next page of results.
	Next *string `json:"next"`

	// HasMore indicates whether there are additional pages available.
	HasMore bool `json:"has_more"`
}

// OK returns a 200 OK response with the provided data.
func OK[T any](c fiber.Ctx, data T) error {
	return c.Status(fiber.StatusOK).JSON(Envelope[T]{Data: data})
}

// Created returns a 201 Created response with the provided data.
func Created[T any](c fiber.Ctx, data T) error {
	return c.Status(fiber.StatusCreated).JSON(Envelope[T]{Data: data})
}

// NoContent returns a 204 No Content response.
func NoContent(c fiber.Ctx) error {
	return c.SendStatus(fiber.StatusNoContent)
}

// Paginated returns a 200 OK response with data and pagination metadata.
func Paginated[T any](c fiber.Ctx, data T, meta *Meta) error {
	return c.Status(fiber.StatusOK).JSON(Envelope[T]{Data: data, Meta: meta})
}

// Error returns a formatted error response following ADR-004.
func Error(c fiber.Ctx, status int, slug, title, detail string) error {
	return c.Status(status).JSON(Envelope[any]{
		Error: &Problem{
			Type:     fmt.Sprintf("https://opus.local/errors/%s", slug),
			Title:    title,
			Status:   status,
			Detail:   detail,
			Instance: c.Path(),
		},
	})
}
